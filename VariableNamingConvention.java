public class VariableNamingConvention { 
    public static void main(String[] args) { 
            int camelCaseNumber = 1; 
            String codingProgram = "WIN"; 
            boolean isCodingCool = true;

            System.out.println("Integer: " + camelCaseNumber);
            System.out.println("String: " + codingProgram);
            System.out.println("Boolean: " + isCodingCool);
    }
}